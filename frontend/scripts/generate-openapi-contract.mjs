import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const frontendRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(frontendRoot, "..");
const swaggerPath = path.join(repoRoot, "api_contracts", "openapi", "swagger.json");
const outputPath = path.join(
  frontendRoot,
  "src",
  "api",
  "contracts",
  "openapi-contract.generated.js",
);

const ANNOTATION_PREFIXES = [
  "/model-hub/annotation-tasks",
  "/model-hub/annotation-queues",
  "/model-hub/annotations-labels",
  "/model-hub/annotations",
  "/model-hub/scores",
  "/tracer/bulk-annotation",
  "/tracer/get-annotation-labels",
  "/tracer/trace-annotation",
  "/tracer/observation-span/add_annotations",
  "/tracer/observation-span/delete_annotation_label",
  "/tracer/project-version/add_annotations",
];

const ANNOTATION_EXACT_PATHS = [
  "/model-hub/dataset/{dataset_id}/annotation-summary/",
];

const FILTER_PREFIXES = [
  "/model-hub/ai-filter",
  "/api/traces",
  "/tracer/dashboard",
  "/tracer/observation-span",
  "/tracer/project",
  "/tracer/trace",
  "/tracer/trace-session",
  "/tracer/users",
];

const hasPrefix = (pathName, prefixes) =>
  prefixes.some((prefix) => pathName === `${prefix}/` || pathName.startsWith(`${prefix}/`));

const isProtectedPath = (pathName) =>
  hasPrefix(pathName, ANNOTATION_PREFIXES) ||
  hasPrefix(pathName, FILTER_PREFIXES) ||
  ANNOTATION_EXACT_PATHS.includes(pathName);

const swagger = JSON.parse(fs.readFileSync(swaggerPath, "utf8"));
const neededDefinitionNames = new Set();

function definitionName(ref) {
  const match = String(ref || "").match(/^#\/definitions\/(.+)$/);
  return match?.[1] || null;
}

function collectRefs(schema) {
  if (!schema || typeof schema !== "object") return;
  if (Array.isArray(schema)) {
    schema.forEach(collectRefs);
    return;
  }
  const name = definitionName(schema.$ref);
  if (name) neededDefinitionNames.add(name);
  Object.values(schema).forEach(collectRefs);
}

function queryParameterSchema(parameter) {
  const schema = {};
  [
    "type",
    "format",
    "enum",
    "items",
    "minimum",
    "maximum",
    "minLength",
    "maxLength",
    "minItems",
    "maxItems",
    "default",
  ].forEach((key) => {
    if (parameter[key] !== undefined) schema[key] = parameter[key];
  });
  return schema;
}

const endpoints = {};

Object.keys(swagger.paths || {})
  .filter(isProtectedPath)
  .sort()
  .forEach((pathName) => {
    const pathSpec = swagger.paths[pathName] || {};
    const methods = {};
    Object.entries(pathSpec).forEach(([method, operation]) => {
      if (method === "parameters") return;
      const parameters = operation.parameters || [];
      const bodyParameter = parameters.find((parameter) => parameter.in === "body");
      const queryParameters = Object.fromEntries(
        parameters
          .filter((parameter) => parameter.in === "query")
          .map((parameter) => [
            parameter.name,
            {
              required: Boolean(parameter.required),
              schema: queryParameterSchema(parameter),
            },
          ]),
      );
      const responses = Object.fromEntries(
        Object.entries(operation.responses || {})
          .filter(([, response]) => response?.schema)
          .map(([statusCode, response]) => [statusCode, response.schema]),
      );

      if (bodyParameter?.schema) collectRefs(bodyParameter.schema);
      Object.values(queryParameters).forEach((parameter) => collectRefs(parameter.schema));
      Object.values(responses).forEach(collectRefs);

      methods[method] = {
        operationId: operation.operationId || null,
        requestBody: bodyParameter?.schema || null,
        queryParameters,
        responses,
      };
    });
    endpoints[pathName] = methods;
  });

const definitions = {};
const pending = [...neededDefinitionNames].sort();
while (pending.length) {
  const name = pending.shift();
  if (definitions[name]) continue;
  const definition = swagger.definitions?.[name];
  if (!definition) continue;
  definitions[name] = definition;

  const before = neededDefinitionNames.size;
  collectRefs(definition);
  if (neededDefinitionNames.size !== before) {
    [...neededDefinitionNames]
      .filter((nextName) => !definitions[nextName] && !pending.includes(nextName))
      .sort()
      .forEach((nextName) => pending.push(nextName));
  }
}

const contract = {
  generatedFrom: "api_contracts/openapi/swagger.json",
  swaggerVersion: swagger.swagger || swagger.openapi || null,
  endpointCount: Object.keys(endpoints).length,
  endpoints,
  definitions,
};

const body = `// DO NOT EDIT THIS FILE DIRECTLY.
// Generated from ../../../../api_contracts/openapi/swagger.json.
// Update Django serializers/views, run the Swagger generator, then run \`yarn contracts:generate\` from frontend.

export const OPENAPI_CONTRACT = Object.freeze(${JSON.stringify(contract, null, 2)});
`;

if (process.argv.includes("--check")) {
  const current = fs.existsSync(outputPath)
    ? fs.readFileSync(outputPath, "utf8")
    : "";
  if (current !== body) {
    console.error(
      "openapi-contract.generated.js is out of date. Run `yarn contracts:generate`.",
    );
    process.exit(1);
  }
  process.exit(0);
}

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, body);
