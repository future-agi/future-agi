/* eslint-env node */
/* eslint-disable no-console */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import prettier from "prettier";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const frontendRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(frontendRoot, "..");
const contractPath = path.join(
  repoRoot,
  "api_contracts",
  "filter_contract.json",
);
const outputPath = path.join(
  frontendRoot,
  "src",
  "api",
  "contracts",
  "filter-contract.generated.js",
);

const contract = JSON.parse(fs.readFileSync(contractPath, "utf8"));
const rawBody = `// DO NOT EDIT THIS FILE DIRECTLY.
// Generated from ../../../../api_contracts/filter_contract.json.
// Update api_contracts/filter_contract.json, then run \`yarn contracts:generate\` from frontend.

export const FILTER_CONTRACT = Object.freeze(${JSON.stringify(contract, null, 2)});

export const FILTER_CONTRACT_VERSION = FILTER_CONTRACT.version;
export const FILTER_OP_ALIASES = Object.freeze(FILTER_CONTRACT.operators.aliases);
export const NO_VALUE_FILTER_OPS = Object.freeze(FILTER_CONTRACT.operators.noValue);
export const LIST_FILTER_OPS = Object.freeze(FILTER_CONTRACT.operators.list);
export const RANGE_FILTER_OPS = Object.freeze(FILTER_CONTRACT.operators.range);
export const SPAN_ATTRIBUTE_ALLOWED_OPS = Object.freeze(FILTER_CONTRACT.operators.spanAttributeAllowed);
export const FILTER_TYPE_ALLOWED_OPS = Object.freeze(FILTER_CONTRACT.operators.filterTypeAllowed);
export const FIELD_TYPE_ALIASES = Object.freeze(FILTER_CONTRACT.fieldTypes.aliases);
export const COLUMN_TYPE_ALIASES = Object.freeze(FILTER_CONTRACT.columnTypes.aliases);
`;
const body = await prettier.format(rawBody, { filepath: outputPath });

if (process.argv.includes("--check")) {
  const current = fs.existsSync(outputPath)
    ? fs.readFileSync(outputPath, "utf8")
    : "";
  if (current !== body) {
    console.error(
      "filter-contract.generated.js is out of date. Run `yarn contracts:generate`.",
    );
    process.exit(1);
  }
  process.exit(0);
}

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, body);
