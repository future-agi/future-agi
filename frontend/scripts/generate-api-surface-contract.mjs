import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const frontendRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(frontendRoot, "..");
const swaggerPath = path.join(repoRoot, "api_contracts", "openapi", "swagger.json");
const outputPath = path.join(
  frontendRoot,
  "src",
  "api",
  "contracts",
  "api-surface.generated.js",
);

const ANNOTATION_PREFIXES = [
  "/model-hub/annotation-tasks",
  "/model-hub/annotation-queues",
  "/model-hub/annotations-labels",
  "/model-hub/annotations",
  "/model-hub/scores",
  "/tracer/bulk-annotation",
  "/tracer/get-annotation-labels",
  "/tracer/trace-annotation",
  "/tracer/observation-span/add_annotations",
  "/tracer/observation-span/delete_annotation_label",
  "/tracer/project-version/add_annotations",
];

const ANNOTATION_EXACT_PATHS = [
  "/model-hub/dataset/{dataset_id}/annotation-summary/",
];

const FILTER_PREFIXES = [
  "/model-hub/ai-filter",
  "/api/traces",
  "/tracer/dashboard",
  "/tracer/observation-span",
  "/tracer/project",
  "/tracer/trace",
  "/tracer/trace-session",
  "/tracer/users",
];

const hasPrefix = (pathName, prefixes) =>
  prefixes.some((prefix) => pathName === `${prefix}/` || pathName.startsWith(`${prefix}/`));

const methodNames = (entry) =>
  Object.keys(entry || {})
    .filter((key) => !["parameters"].includes(key))
    .sort();

const swagger = JSON.parse(fs.readFileSync(swaggerPath, "utf8"));
const paths = swagger.paths || {};

const buildGroup = (prefixes, exactPaths = []) =>
  Object.fromEntries(
    Object.keys(paths)
      .filter((pathName) => hasPrefix(pathName, prefixes) || exactPaths.includes(pathName))
      .sort()
      .map((pathName) => [pathName, methodNames(paths[pathName])]),
  );

const contract = {
  generatedFrom: "api_contracts/openapi/swagger.json",
  swaggerVersion: swagger.swagger || swagger.openapi || null,
  groups: {
    annotation: buildGroup(ANNOTATION_PREFIXES, ANNOTATION_EXACT_PATHS),
    filter: buildGroup(FILTER_PREFIXES),
  },
};

const body = `// DO NOT EDIT THIS FILE DIRECTLY.
// Generated from ../../../../api_contracts/openapi/swagger.json.
// Update Django serializers/views, run the Swagger generator, then run \`yarn contracts:generate\` from frontend.

export const API_SURFACE_CONTRACT = Object.freeze(${JSON.stringify(contract, null, 2)});

export const API_SURFACE_PATHS = Object.freeze({
  ...API_SURFACE_CONTRACT.groups.annotation,
  ...API_SURFACE_CONTRACT.groups.filter,
});
`;

if (process.argv.includes("--check")) {
  const current = fs.existsSync(outputPath)
    ? fs.readFileSync(outputPath, "utf8")
    : "";
  if (current !== body) {
    console.error(
      "api-surface.generated.js is out of date. Run `yarn contracts:generate`.",
    );
    process.exit(1);
  }
  process.exit(0);
}

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, body);
